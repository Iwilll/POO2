package jajasx2;

public class Materia {
	
	private int creditos;
	private int iD;
	
	
	
	public Materia(int creditos, int iD) {
		this.creditos = creditos;
		this.iD = iD;
	}

	public int getCreditos() {
		return creditos;
	}

	public void setCreditos(int creditos) {
		this.creditos = creditos;
	}

	public int getiD() {
		return iD;
	}

	public void setiD(int iD) {
		this.iD = iD;
	}
	
	
	
}
